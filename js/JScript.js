function  offmenu()
{
    var sidebar=document.getElementsByClassName("side-bar");
    var main=document.getElementsByClassName("main-body");
    if(a==0)
    {
    sidebar[0].style.transform="translate(-100%  , 0%)"
    sidebar[0].style.display="block"
    main[0].style.marginLeft="0px"
a=1
    }
    else
    {
    sidebar[0].style.transform="translate(0%  , 0%)"
    sidebar[0].style.display="block"
    main[0].style.marginLeft="180px"
    a=0;  
    }

}




